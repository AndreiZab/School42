#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	if (n == -2147483648)
	{
		ft_putnbr(-2);
		ft_putnbr(147483648);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
		return ;
	}
	i = n;
	while (i >= 10)
		i = i % 10;
	if (n >= 10)
		ft_putnbr(n / 10);
	i = i + '0';
	write(1, &i, 1);
}
