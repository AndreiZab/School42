#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	if (n == -2147483648)
	{
		ft_putnbr_fd(-2, fd);
		ft_putnbr_fd(147483648, fd);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
		return ;
	}
	i = n;
	while (i >= 10)
		i = i % 10;
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	i = i + '0';
	write(fd, &i, 1);
}
