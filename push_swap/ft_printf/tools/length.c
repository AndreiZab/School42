#include "../ft_printf.h"

int	length(char *str, char c)
{
	int		i;
	int		nb;

	i = 0;
	nb = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
		{
			nb++;
		}
		i++;
	}
	return (nb);
}
