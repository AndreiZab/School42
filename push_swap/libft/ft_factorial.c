int		ft_factorial(int nb)
{
	int fu;

	fu = 1;
	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while (nb != 0)
	{
		fu = nb * fu;
		nb = nb - 1;
	}
	return (fu);
}
