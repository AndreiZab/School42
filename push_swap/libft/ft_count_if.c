int		ft_count_if(char **tab, int (*f)(char*))
{
	int j;
	int k;

	j = 0;
	k = 0;
	while (tab[j])
	{
		if ((*f)(tab[j]) == 1)
			k++;
		j++;
	}
	return (k);
}
