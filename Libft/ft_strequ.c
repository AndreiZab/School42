#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t i;

	if (s2 == NULL || s1 == NULL)
		return (0);
	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	return (1);
}
